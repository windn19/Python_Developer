print('Task 6')
'''
Найти сумму цифр числа.
'''
n = 0  # ввод начального значения
s = int(input('Введите число: '))  # ввод числа пользователем
while s // 10 != 0:  # пока результат деления числа на 10 не равен 0 делай
    n += s % 10  # к n прибавляется остаток от деления числа на 10
    s //= 10  # само число делится на 10
else:  # при выходе из цикла
    n += s  # добавить значение последней цифры
print(f'Сумма цифр числ: {n}', end='\n')  # вывод результата

